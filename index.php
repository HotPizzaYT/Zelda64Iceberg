There are many mysteries surrounding The Legend of Zelda, Ocarina of Time. One of them happens to be that every copy on the 64DD is personalizeds<br>
<p>Why do I say this?</p>
Well, it's not very known but Nintendo had made a partnership with Don't Ask Software in 1997 to create an AI of a previously deceased Nintendo employee.
<br> Ever since then, every official copy of Ocarina of Time for the 64DD has been cursed.
<br>
<br>
There are anomalous areas such as the <b>sewer</b> which is only accessible in personalized copies through playing Zelda's Lullaby in front of the fetuses in the Lakeside Laboratory.
<br>
I don't reccommend going there as it can cause stroke like symptoms and various mental illnesses. If you do, however, manage to find the triforce in the sewer the game will present you with an endscreen, similar to Super Mario 64's
